    public StudentHomeForm(String userid, String accesstype) {
        this.connect = new Mysqlconnection();       //create a connection object and intialize
        currentUser=new User();
        currentUser.setUserID(userid);
        currentUser.setAccess_Type(accesstype);
        added_to_db=false;
        initComponents();
          String term_condition="";
          String credits="";
        try {
            String yos=connect.getspecificdata(currentUser.getAccess_Type(), "id", currentUser.getUserID(), "Year of Study");       //set the year of study
            int yos_int=Integer.parseInt(yos);
            String sos=connect.getspecificdata(currentUser.getAccess_Type(), "id", currentUser.getUserID(), "Semester of Study");   //set the semester of study
            int sos_int=Integer.parseInt(sos);
            String degree=connect.getspecificdata(currentUser.getAccess_Type(), "id", currentUser.getUserID(), "Degree");           //set the degree of User
            this.student_Curriculum=new Curriculum(degree, currentUser.getUserID(),yos_int, sos_int);
            term_condition = connect.getspecificdata(currentUser.getAccess_Type(), "id", currentUser.getUserID(), "Term Condition");
            connect.setuptable(past_reults_table, "modules.`students past`", "Module,Mark,`Semester taken`,`Year taken`,Rating", "`Student Number`", currentUser.getUserID());
            if(connect.checkuserdb("modules.`current curriculum`", "id" , currentUser.getUserID())){
                connect.setuptable(current_curriculum_table, "modules.`current curriculum`", "`Module`,Credits", "id", currentUser.getUserID());    //set up the current curriculum table
            }
            connect.setup2vartable(standard_curriculum_table, "modules.`"+degree+"`", "`Module code`, `Module name`,Credits, Pre_requisites, Co_requisites","Year",yos,"Semster",sos);      //set up standard curriculum table
            credits=connect.getspecificdata(currentUser.getAccess_Type(), "id", currentUser.getUserID(), "Credits");
        } 
        catch (Exception ex) {
            Logger.getLogger(StudentHomeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        termcondition_textbox.setText(term_condition);
        total_credits_textbox.setText(credits);
        this.dispose();
    }

