    private void add_curricululm_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_curricululm_buttonActionPerformed
        if(added_to_db==false){
        if(JOptionPane.showConfirmDialog(this, "Are you sure that you want to add this curriculum?", "Confirmation", JOptionPane.YES_NO_OPTION)==0){    //confirm if the user wants to add the generated curriculum
        for(int count=0;count<10 && student_Curriculum.getModulesTaken(count)!=null;count++){       //get all the inputs from the table
            String idvalue=currentUser.getUserID();
            idvalue=idvalue.concat("_");
            idvalue=idvalue.concat(student_Curriculum.getModulesTaken(count));
            String student_num=currentUser.getUserID();
            String module=student_Curriculum.getModulesTaken(count);
            String year_taken=String.valueOf(student_Curriculum.getYear());
            String semester_taken=String.valueOf(student_Curriculum.getSemester());
            String Credits="0";
            String query_values="0";
            try {
                Credits=connect.getspecificdata("modules.`"+student_Curriculum.getDegree()+"`", "`Module code`",student_Curriculum.getModulesTaken(count) , "Credits");     //get the credits of each value in the generated curriculum
            } catch (Exception ex) {
                Logger.getLogger(StudentHomeForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            query_values="'"+idvalue+"','"+student_num+"','"+module+"','"+year_taken+"','"+semester_taken+"','"+Credits+"'";
            try {
                connect.addtodb("modules.`current curriculum`", query_values);                      //add modules to the current curriculum database table
            } catch (Exception ex) {
                Logger.getLogger(StudentHomeForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
                    
            }
            JOptionPane.showMessageDialog(this, "The generated curriculum has been added to the system", "Curriculum added", JOptionPane.INFORMATION_MESSAGE);
            added_to_db=true;
        }
        }
        else JOptionPane.showMessageDialog(this, "The generated curriculum has already been added to the system", "Curriculum added", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_add_curricululm_buttonActionPerformed

    private void btnGenerateCurriculumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateCurriculumActionPerformed
        try {
            if(connect.checkuserdb("modules.`current curriculum`", "id" , currentUser.getUserID())){
                JOptionPane.showMessageDialog(this, "A curriculum has already been generated and assigned.", "Curriculum already established",JOptionPane.INFORMATION_MESSAGE);     //display a message if a curriculum has already been generated
            }
            else if(student_Curriculum.getModulesTaken(0)!=null){
                 JOptionPane.showMessageDialog(this, "A curriculum has already been generated.", "Curriculum already generated",JOptionPane.INFORMATION_MESSAGE);       
            }
            else{
                student_Curriculum.generatecurriculum();        //if a curriculum has not been generated yet, generate it
                for(int count=0;count<10;count++){
                    System.out.print(student_Curriculum.getModulesTaken(count));
                }
                DefaultTableModel model= (DefaultTableModel) current_curriculum_table.getModel();
                for(int count=0;count<10 && student_Curriculum.getModulesTaken(count)!=null;count++){
                    model.insertRow(model.getRowCount(), new Object[]{student_Curriculum.getModulesTaken(count),Integer.parseInt(connect.getspecificdata("modules.`"+student_Curriculum.getDegree()+"`", "`Module code`",student_Curriculum.getModulesTaken(count) , "Credits"))});
                }
                Curriculum_credits_field.setText(String.valueOf(student_Curriculum.getTotalCredits()));
            }
        } catch (Exception ex) {
            Logger.getLogger(StudentHomeForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGenerateCurriculumActionPerformed

